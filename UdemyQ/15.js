// Parametors  y argumentos  en funciones al ser llamadas

function sumar( a, b) { // parametos 
    console.log(a * b )
}
sumar( 10,3) // argumento



function sumar( a1, b2) { // parametos 
    console.log(a1 + b2 )
}
sumar(3, 2)

const sumar2 = function(a2, b1) {
    console.log(a2 + b1)
}
sumar2(5, 4)
